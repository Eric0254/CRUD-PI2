  /**
 * Exibe o valor total dos dados presentes na tabela de relatório sintético.
 *
 * @param status true se o valor total deve ser calculado como soma, false se deve ser calculado como subtração.
 */
    
private void exibirTotal(boolean status) {
    double valorTotal = 0;
    int cont = RelatorioSintetico.getRowCount();

    if (status) {
        for (int i = 0; i < cont; i++) {
            valorTotal += Double.parseDouble(RelatorioSintetico.getValueAt(i, 2).toString());
        }

        lblValorTotal.setText(String.valueOf(valorTotal));
    } else {
        for (int i = 0; i < cont; i++) {
            valorTotal -= Double.parseDouble(RelatorioSintetico.getValueAt(i, 2).toString());
        }

        lblValorTotal.setText(String.valueOf(valorTotal));
    }
}
    

