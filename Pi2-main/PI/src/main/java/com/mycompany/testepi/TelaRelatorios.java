    /**
     * Função que pesquisa e emite o relatorio sintético
     *
     * @param evt
     */
    
    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
   
        ArrayList<String> erros = new ArrayList();
        
        if (jdcDIN.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Insira a data do periodo");
            return;
        }
        if (jdcDF.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Insira a data do periodo");
            return;
        }
        
        // Obtenha as datas inicial e final selecionadas nos JDateChoosers
Date dataInicial = jdcDIN.getDate();
Date dataFinal = jdcDF.getDate();

// Verifique se as datas selecionadas são válidas
if (dataInicial != null && dataFinal != null) {
    // Converta as datas para o formato adequado para consulta no banco de dados
    java.sql.Date sqlDataInicial = new java.sql.Date(dataInicial.getTime());
    java.sql.Date sqlDataFinal = new java.sql.Date(dataFinal.getTime());

    // Consulte o banco de dados para obter os relatórios com as datas selecionadas
    ArrayList<Relatorio> lista = RelatorioDAO.listar(sqlDataInicial, sqlDataFinal);

    if (lista != null) {
        DefaultTableModel modelo = (DefaultTableModel) RelatorioSintetico.getModel();
        modelo.setRowCount(0);

        for (Relatorio relatorio : lista) {
            modelo.addRow(new String[]{
                String.valueOf(relatorio.getData()),
                String.valueOf(relatorio.getCliente()),
                String.valueOf(relatorio.getValor())
            });
        }
    }
}
exibirTotal(true);
    }//GEN-LAST:event_btnPesquisarActionPerformed
 
