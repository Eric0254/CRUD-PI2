import java.util.ArrayList;
import java.util.Date;

public class Venda {
    private int codVenda;
    private int quantidade;
    private double valorUnitario;
    private double valorTotal;
    private int codProduto;
    private String nomeProduto;
    private String cpf;
    private String nome;
    private String tipo_pag;
    private String parcelamento;
    private double Final;
    private Date dataVenda;

    public Venda() {
    }

    public int getCodVenda() {
        return codVenda;
    }

    public void setCodVenda(int codVenda) {
        this.codVenda = codVenda;
    }

    public int getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(int quantidade) {
        this.quantidade = quantidade;
    }

    public double getValorUnitario() {
        return valorUnitario;
    }

    public void setValorUnitario(double valorUnitario) {
        this.valorUnitario = valorUnitario;
    }

    public double getValorTotal() {
        return valorTotal;
    }

    public void setValorTotal(double valorTotal) {
        this.valorTotal = valorTotal;
    }

    public int getCodProduto() {
        return codProduto;
    }

    public void setCodProduto(int codProduto) {
        this.codProduto = codProduto;
    }

    public String getNomeProduto() {
        return nomeProduto;
    }

    public void setNomeProduto(String nomeProduto) {
        this.nomeProduto = nomeProduto;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTipo_pag() {
        return tipo_pag;
    }

    public void setTipo_pag(String tipo_pag) {
        this.tipo_pag = tipo_pag;
    }

    public String getParcelamento() {
        return parcelamento;
    }

    public void setParcelamento(String parcelamento) {
        this.parcelamento = parcelamento;
    }

    public double getFinal() {
        return Final;
    }

    public void setFinal(double Final) {
        this.Final = Final;
    }

    public Date getDataVenda() {
        return dataVenda;
    }

    public void setDataVenda(Date dataVenda) {
        this.dataVenda = dataVenda;
    }

    private ArrayList<Venda> listaItens = null;

    public ArrayList<Venda> getListaItens() {
        return listaItens;
    }

    public void setListaItens(ArrayList<Venda> listaItens) {
        this.listaItens = listaItens;
    }
    
    
    
    
}

